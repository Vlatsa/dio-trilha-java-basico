public class tipoVariaveis {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");

        String nome = "Lucas"; 
        System.out.println(nome);

        char sexo = 'M'; // 0 a 65535
        System.out.println(sexo);

        boolean ativo = true; // true ou false
        System.out.println(ativo);

        float salarioFloat = 1000.00f; // 1.4E-45 a 3.4028235E38
        System.out.println(salarioFloat);

        double salario = 1000.00; // 4.9E-324 a 1.7976931348623157E308
        System.out.println(salario);

        byte idadeByte = 20; // -128 a 127
        System.out.println(idadeByte);

        short idadeShort = 20; // -32.768 a 32.767
        System.out.println(idadeShort);

        int idade = 20; // -2.147.483.648 a 2.147.483.647
        System.out.println(idade);

        long idadeLong = 20; // -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807
        System.out.println(idadeLong);
        
        //estudar
            //Tipos Primitivos de Variaveis
            //Classe String que representa texto na aplicação
      

    }
}
